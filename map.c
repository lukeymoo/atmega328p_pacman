// Stores all the `points` & `abilities` tiles in an array
// This array will be used for drawing and determining which have been
// collected/used & should be discarded for use in game logic
void init_map(void)
{
	uint8_t index = 0;
	
	// Initialize object map
	for(uint8_t i = 0; i < 128; i++) {
		OBJECT_MAP[i].x = 0;
		OBJECT_MAP[i].y = 0;
		OBJECT_MAP[i].type = 0xff;
		OBJECT_MAP[i].active = 0;
	}
	
	// Go through map & count each of type
	for(uint8_t y = 0; y < 8; y++) {
		for(uint8_t x = 0; x < 16; x++) {
			// Point
			if(pgm_read_byte(&COLLISION_MAP[y][x]) == POINT) {
				// assign object to point
				OBJECT_MAP[index].x = x;
				OBJECT_MAP[index].y = y;
				OBJECT_MAP[index].type = POINT;
				OBJECT_MAP[index].active = 1;
				index++;
			}
			if(pgm_read_byte(&COLLISION_MAP[y][x]) == ABILITY) {
				// assign object to point
				OBJECT_MAP[index].x = x;
				OBJECT_MAP[index].y = y;
				OBJECT_MAP[index].type = ABILITY;
				OBJECT_MAP[index].active = 1;
				index++;
			}
			if(pgm_read_byte(&COLLISION_MAP[y][x]) == GATE) {
				OBJECT_MAP[index].x = x;
				OBJECT_MAP[index].y = y;
				OBJECT_MAP[index].type = GATE;
				OBJECT_MAP[index].active = 1;
				index++;
			}
			if(pgm_read_byte(&COLLISION_MAP[y][x]) == WALL) {
				OBJECT_MAP[index].x = x;
				OBJECT_MAP[index].y = y;
				OBJECT_MAP[index].type = WALL;
				OBJECT_MAP[index].active = 1;
				index++;
			}
		}
	}
	return;
}

GAME_OBJECT *find_object(uint8_t x, uint8_t y) {
	for(uint8_t i = 0; i < 90; i++) {
		if(OBJECT_MAP[i].x == x && OBJECT_MAP[i].y == y) {
			return &OBJECT_MAP[i];
		}
	}
	
	return NULL;
}